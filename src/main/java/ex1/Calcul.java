package ex1;

/**
 * La classe Calcul permet d'effectuer des opérations arithmétiques de base, 
 * telles que l'addition et la soustraction.
 */
public class Calcul {

    /**
     * Additionne deux entiers.
     *
     * @param a le premier entier
     * @param b le deuxième entier
     * @return la somme de a et b
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Soustrait le deuxième entier du premier entier.
     *
     * @param a le premier entier
     * @param b le deuxième entier
     * @return la différence entre a et b
     */
    public int subtract(int a, int b) {
        return a - b;
    }
}
